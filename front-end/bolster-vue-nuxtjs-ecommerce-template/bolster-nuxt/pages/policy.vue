                <br>- 결제정보: 판매자는 회사가 제공한 결제방법 중 구매자가 결제시 사용할 수 있는 결제방법을 명확히 기재하여야 합니다.
                <br>- 운송료: 운송료는 구매자 부담여부, 부담조건, 배송방법을 구매자가 명확히 알고 선택할 수 있도록 회사가 허용하는 운송료 부담 조건 중 하나를 선택하고 상세 운송조건을
                기재하여야
                합니다.
                <br>- 상품 등의 정보: 판매자는 상품 등록 시 ① 품목별 재화 등에 관한 정보 및 ② 거래조건에 관한 정보 등 ‘전자상거래 등에서의 상품 등의 정보제공에 관한
                고시’(공정거래위원회
                고시 제2012-49호, 이하 ‘상품정보제공고시’)에서 정한 정보를 입력하여야 하고, 상품 등록 후 상품정보제공고시가 변경되는 경우 그에 맞추어 관련 정보를 수정, 보완하여야
                합니다.
                <li>판매자가 상품정보제공고시에 따른 정보를 입력하지 않거나, 상품 등록 후 변경된 상품정보제공고시에 따라 정보를 수정, 보완하지 않는 경우 회사는 제15조 나. 7에 따라
                    회원자격의
                    정지,
                    서비스 제공 거부ㆍ제한 등 필요한 조치를 취할 수 있습니다.</li>
                판매자는 등록물품에 특별한 거래조건이 있거나 교환, 반품비용 등 추가되는 비용이 있는 경우, 이를 구매자가 알 수 있도록 명확하게 기재하여야 합니다. 다만, 이러한 특별한
                거래조건은
                구매자의 정당한 권리를 제한하거나 이 약관을 위반하거나 기타 회사가 미리 정한 거래조건에 위배되어서는 안됩니다.
                <li>물품등록 이후에는 금액 이외의 수정이 불가능합니다. 즉시구매가격은 마감임박시점까지는 일정가격 범위(변경 전의 즉시구매가격과 최고 입찰가격 간의 범위를 말합니다.) 내에서
                    수정이
                    가능합니다. 수정되거나 추가된 내용이 입찰자 또는 구매자에게 불리한 경우 추가내용이 등록되기 이전에 입찰한 입찰자 또는 구매자에게는 이전의 내용이 적용됩니다.</li>
                <li>판매자는 물품정보를 등록함에 있어서 저작권, 초상권 등 제3자의 권리를 침해해서는 안 됩니다. 판매자가 등록한 물품정보는 판매 활성화를 위하여 회사가 제휴한 사이트와 다른
                    회원의
                    블로그
                    등에 노출될 수 있습니다. 다만, 다른 회원의 블로그 등에의 노출은 다른 회원이 회사가 정한 이용규칙에 동의하고 회사가 허용한 방식으로 퍼가기 하는 경우에만 가능합니다.
                </li>
            </ol>
            3) 부가서비스의 이용
            <br>- 판매자는 물품등록, 주문정보처리, 서비스이용료 정산 등 판매관리 합리화나 등록물품에 대한 광고 효과를 높이기 위하여 회사 또는 회사가 위탁한 제3자가 제공하는 부가서비스를
            이용할 수
            있습니다.
            부가서비스에는 과금 여부에 따라 유료의 부가서비스와 무료의 부가서비스가 있고 서비스 내용에 따라 광고성 부가서비스와 특별 부가서비스가 있습니다.
            <br>- 부가서비스의 내용과 이용절차는 별도의 서비스 화면에 게재합니다. 부가서비스의 종류나 내용, 이용료는 변경될 수 있습니다.
            <br>4) 물품등록정보의 게재
