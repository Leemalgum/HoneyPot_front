            조정할 수
            있으며, 이벤트 광고 등 회사의 서비스를 위하여 해당 서비스화면을 구성, 편집하거나 물품등록정보 외의 사항을 게재할 수 있습니다.
            <br>5) 물품이 회사에서 검수 과정을 거치고 검수 과정에 통과해 하자 없음으로 판별된 제품만이 정식 입고 처리되며, 하자 발견 및 가품판별 등의 사유로 등록 실패한 물품들은 등록된
            주소로
            반송처리 됩니다.
            </p>

            <h3>제31조 (물품판매규정)</h3>
            <p>1) 경매는 판매자가 기재한 경매기간까지 진행됩니다. 다만, 회사에 의하여 직권으로 해당 경매가 취소되는 경우 등에는 조기에 종료될 수 있습니다.
                <br>2) 경매가 종료되면, 회사는 낙찰 후 마이페이지 서비스화면에 낙찰 여부를 게시하거나 e-mail 등의 방법으로 판매자에게 통지합니다. 판매자는 회사가 보내는 낙찰 확인
                e-mail의 수신 또는마이페이지 화면을 통하여 낙찰을 확인할 수 있습니다.
                <br>3) 회사는 꿀거래 진행 중 구매자가 입금 또는 물품대금을 결제하면 즉시 판매자에게 입금확인 및 물품발송통지를 보냅니다. 판매자는 구매자의 입금 여부를 회사에서 보내는
                입금확인
                e-mail이나 마이페이지 화면을 통해 확인할 수 있습니다.
                <br>4) 판매자는 회사로부터 구매자의 입금 또는 대금결제에 대한 확인통지를 받은 후에는 이 약관 제24조에서 정하고 있는 배송절차를 취하여야 합니다.
                <br>5) 판매자가 물품등록시 운송료를 구매자부담으로 기재하면서 배송비 선결제를 선택하고 구매자가 이에 동의한 경우, 이 약관 제21조 내지 제26조에 따라 구매자가 입금 또는
                결제하거나 회사가 송금 또는 환불(반품의 경우)하는 물품대금에는 선결제 배송비도 포함된 것으로 합니다.
                <br>6) 경매가 낙찰로 종료된 경우, 회사는 구매자에게 물품을 배송합니다. 이후 구매자는 배송완료가 된 이후 5영업일 이내에 구매확정 버튼을 누르거나 상품에 문제가 있는 경우
                기간
                안에 고지하여야 하며, 기간이 지난 이후에는 자동으로 구매 확정 처리됩니다.
                <br>7) 꿀거래가 종료된 경우, 회사는 물품대금의 입출금에 관한 매매보호서비스만을 제공합니다. 판매자는 그 외 물품의 배송, 반품, A/S 등에 관련한 사항은 항상 구매자와
                협의하여
                진행해야 하며, 회사는 판매자와 구매자간의 거래에 관여하지 않습니다.
            </p>

            <h3>제32조 (물품구매규정)</h3>
            <p>1) 물품을 구매하고자 하는 회원은 회사가 제공하는 서비스화면에서 입찰하여야 합니다. 입찰에 대하여는 서비스이용료가 부과되지 않습니다.
                <br>2) 입찰/구매신청방법
                <br>- 회원은 물품등록정보를 자세히 살펴본 후 구매의사가 있을 때에 한하여 신중히 입찰(또는 구매신청)하여야 합니다.
                <br>- 회원은 경매진행 중에는 고객센터에 있는 [문의게시판]을 통해 물품에 관한 질문을 할 수 있습니다.
                <br>- 입찰은 지정된 경매마감일시까지 여러 번 할 수 있으나, 연속적인 중복입찰은 허용되지 않습니다. 즉시구매의 경우에는 최초의 구매신청이 이루어진 이후에도 경매가 진행중인
                경우에는
                추가구매를 할 수 있습니다. 입찰가격은 현재가에서 선정된 입찰폭 이상의 금액부터 자유롭게 책정할 수 있습니다.
                <br>- 일반경매에서 입찰을 한 회원은 경매가 낙찰될 때까지는 판매자의 승낙 없이는 임의로 입찰을 철회할 수 없습니다.
                <br>3) 구매과정
                <br>- 구매자는 경매종료 후 회사가 24시간 이내에 발송하는 낙찰확인 e-mail 또는 마이페이지에서 낙찰 여부를 확인할 수 있습니다.
                <br>- 즉시구매가가 설정된 경매는 경매기간이 만료하기 이전에 [(즉시)구매하기] 항목을 클릭하고 결제를 완료하면 즉시 낙찰이 이루어집니다. 다만, 무통장입금의 방식으로 결제하는
                경우에는 주문서 작성 즉시 낙찰이 이루어집니다.
                <br>- 구매자는 낙찰된 이후에도 서비스화면에서 [구매거부] 또는 [결제취소] 항목을 클릭함으로써 구매를 거부할 수 있습니다. 이러한 구매거부에 대해서는 이 약관 제15조가
                적용됩니다.
                다만, 물품발송지연의 사유로 구매자가 [결제취소]항목을 클릭한 경우에는 구매거부로 처리되지 않습니다.
                <br>- 구매자는 일반경매의 경우 낙찰 후 3일(토요일, 공휴일 제외), 주문서 작성으로써 낙찰이 이루어지는 즉시구매의 경우 낙찰 후 5일(토요일, 공휴일 제외), 이내에
                물품대금을
                회사에 입금하여야 합니다. 해당기간이 경과하도록 입금이 확인되지 않는 경우에 구매자의 구매거부로 간주되며 해당 경매는 취소됩니다. 이러한 구매거부에 대해서는 이 약관 제15조가
                적용됩니다.
                <br>- 구매자는 회사가 제공하는 서비스화면에 주문사항, 물품수령지 정보를 정확히 기재하여야 하며, 미기재 또는 오기로 인한 불이익은 구매자가 부담합니다.
                <br>4) 미성년자인 회원이 법정대리인의 동의 없이 물품을 구매하는 경우 본인 또는 법정대리인이 이를 취소할 수 있습니다. 이 경우 회사는 해당 경매를 취소합니다. 다만, 회사가
                이
                약관에서 정한 바에 따라 물품대금을 판매자에게 송금한 이후에는 구매자는 회사에 대하여 이의를 제기할 수 없고 판매자와 사이에 직접 거래취소, 대금환불 등의 모든 문제를 해결하여야
                합니다.
            </p>

            <h3>제33조 (구매자의 입금 또는 대금결제)</h3>
            <p>1) 구매자는 물품대금을 입금하는 경우 매매보호 서비스에 따라 회사에 입금하여야 하고, 입금정보를 회사에서 제공하는 양식에 따라 마이페이지의 해당 서비스화면에 표시하여야 합니다.
                은행
                온라인 입금방법으로 물품대금을 입금하는 경우 구매자명과 입금자명이 서로 상이한 경우에는 입금자명란에 구매자명을 명확히 기재하여야 합니다. 구매자가 물품대금을 잘못 입금하거나
                입금정보를
                잘못 기입하거나 기입하지 않음으로 인한 모든 위험과 책임은 구매자가 부담하여야 하고, 회사는 이에 대하여 아무런 책임을 지지 않습니다.
                <br>2) 구매자는 판매자가 물품등록정보에서 신용카드 결제를 허용하는 경우에 한하여 본인 명의 신용카드로 대금을 결제할 수 있습니다. 회사는 구매자가 제공한 회원정보가 신용카드의
                명의인과 다른 경우 신용카드 결제를 거부하거나 취소할 수 있습니다. 다만, 법인카드의 경우 실명확인절차를 거쳐 사용할 수 있습니다.
            </p>

            <h3>제34조 (배송)</h3>
            <p>1) 꿀거래 판매자는 결제가 확인이 되면 발송예정일 또는 발송관련 정보 등(발송일, 택배사, 송장번호 등)을 입력하여야 하고, 물품발송이 지연될 가능성이 있는 경우 이를 구매자에게
                통지하여야 합니다. 전자상거래 등에서의 소비자보호에 관한 법률 제15조 제1항에 의거하여 판매자는 구매자의 결제일로부터 3 영업일 이내에 상품의 발송을 완료하여야 하고, 물품발송
                전에
                구매자가 주문 또는 결제를 취소하지 않은 것을 확인하여야 합니다. 판매자가 기한 내에 상품을 발송하지 않은 경우 페널티를 받을 수 있습니다. 판매자는 물품발송시, 서비스화면에
                송장번호 등
                물품 발송과 관련된 데이터를 입력하여 발송이 완료되었음을 증명하여야 합니다.
                <br>2) 판매자가 발송예정일 또는 발송관련 정보를 제공한 경우, 회사는 구매자가 이러한 사실을 알 수 있도록 마이페이지에 표기하거나 e-mail 등으로 통지합니다. 판매자가
                물품발송하기 전에 구매자로부터 채팅을 통해 주소 변경으로 인한 수정요청을 받은 경우, 판매자는 직접 구매자의 주문정보를 수정할 수 있습니다.
                <br>3) 판매자가 물품을 배송할 경우 구매자가 물품을 수령하기까지의 위험은 판매자가 부담합니다. 이에 따라 판매자는 구매자가 하자(파손,멸실,부패 등)없는 물품을 수령할 수
